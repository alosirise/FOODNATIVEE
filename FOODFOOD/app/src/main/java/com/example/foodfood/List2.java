package com.example.foodfood;

public class List2 {
    private String name;
    private String pic;

    public List2(String name, String pic) {
        this.name = name;
        this.pic = pic;
    }

    public List2() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }


}
